# Operaciones matemáticas básicas
a = 10
b = 20
c = 30
# Suma
suma = a + b + c
print(f"La suma de {a}, {b} y {c} es: {suma}")

# Resta
resta = c - b - a
print(f"La resta de {c}, {b} y {a} es: {resta}")

# Multiplicación
multiplicacion = a * b * c
print(f"La multiplicación de {a},  {b} y {c} es: {multiplicacion}")

# División
division = a / (b * c)
print(f"La división de {a} entre {b} multiplicado por {c} es: {division}")
